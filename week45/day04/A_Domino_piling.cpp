#include <bits/stdc++.h>
using namespace std;

int main()
{
    int m,n;
    cin>>m>>n;

    int mx=m*n;
    int ans=0;
    int cnt=0;
    while(ans<mx)
    {
       
        ans+=(2*1);
        if(ans<=mx)
        {
            cnt++;
        }
    }
    cout<<cnt<<endl;

    return 0;
}